#include <errno.h>
#include <fcntl.h>
#include <stdio.h>
#include <string.h>
#include <unistd.h>

#include "tnc2.h"

int
tnc2_init(const char *path, speed_t speed, const char *hbaud)
{
	int fd;
	struct termios tty;

	fd = open(path, O_RDWR | O_NOCTTY | O_SYNC);
	if (fd < 0 || tcgetattr(fd, &tty) < 0)
	{
		fprintf(stderr, "Error opening %s: %s\n", path, strerror(errno));
		return errno;
	}

	cfsetospeed(&tty, speed);
	cfsetispeed(&tty, speed);

	tty.c_cflag |= (CLOCAL | CREAD);    /* ignore modem controls */
	tty.c_cflag &= ~CSIZE;
	tty.c_cflag |= CS8;         /* 8-bit characters */
	tty.c_cflag &= ~PARENB;     /* no parity bit */
	tty.c_cflag &= ~CSTOPB;     /* only need 1 stop bit */

	/* setup for non-canonical mode */
	tty.c_iflag &= ~(IGNBRK | BRKINT | PARMRK | ISTRIP | INLCR | IGNCR | ICRNL | IXON);
	tty.c_lflag &= ~(ECHO | ECHONL | ICANON | ISIG | IEXTEN);
	tty.c_oflag &= ~OPOST;

	/* fetch bytes as they become available */
	tty.c_cc[VMIN] = 1;
	tty.c_cc[VTIME] = 5;

	if (tcsetattr(fd, TCSANOW, &tty) != 0)
	{
		fprintf(stderr, "Failed to set up serial port.\n");
		close(fd);
		return -1;
	}

	dprintf(fd, "TC 1\rTN 2,1\r\n"); /* turn on Kenwood radio TNC */
	sleep(1);

	if (hbaud)
		dprintf(fd, "HBAUD %s\r", hbaud);

	dprintf(fd, "KISS ON\rRESTART\r\n");
	sleep(2);

	close(fd);
	return 0;
}
